import UIKit

public extension UIColor {
    
    class func LXBlue() -> UIColor {
        return UIColor(red:3/255, green:60/255, blue:209/255, alpha:1.0)
    }
    
    class func LXGrey() -> UIColor {
        return UIColor(red:86/255, green:98/255, blue:105/255, alpha:1.0)
    }
}
